def normalize_kanji(char: str) -> str:
    """旧字体→新字体に変換。新字体ならそのまま返す。"""
    return OLD_TO_NEW.get(char, char)


def normalize_text(text: str) -> str:
    """テキスト中の旧字体を全て新字体に変換する。"""
    return "".join(normalize_kanji(c) for c in text)


def get_similar_kanji(char: str) -> list[str]:
    """
    OCR誤認しやすい類似漢字の候補を返す。
    旧字体は除外（自動変換されるため候補に出す意味がない）。
    """
    candidates = SIMILAR_KANJI.get(char, set())
    # 旧字体を除外して返す
    return [c for c in candidates if c not in OLD_TO_NEW]
