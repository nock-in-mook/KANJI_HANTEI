# 漢字→学年のマッピング
KANJI_TO_GRADE = {}

for grade, chars in KANJI_BY_GRADE.items():
    for c in chars:
        ALL_EDUCATION_KANJI.add(c)
        KANJI_TO_GRADE[c] = grade


def is_education_kanji(char: str) -> bool:
    """教育漢字かどうか判定"""
    return char in ALL_EDUCATION_KANJI


def get_grade(char: str) -> int:
    """漢字の学年を返す。教育漢字でなければ0。"""
    return KANJI_TO_GRADE.get(char, 0)


